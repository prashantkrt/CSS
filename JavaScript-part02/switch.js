let color = "Red";

switch (color) {
  case "Red":
    console.log("Color is Red");
    break;
  case "Blue":
    console.log("Color is Blue");
    break;
  case "Pink":
    console.log("Color is pink");
    break;
  default:
    console.log("None of the color matched");
}

